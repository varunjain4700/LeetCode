class Solution{
		

	public:
	int minOperations(string str1, string str2) 
	{ 
	    int m = str1.size(), n = str2.size();
	    vector<int> curr(n + 1, 0), prev(n + 1, 0);
            for (int i = 1; i <= m; i++)
            {
                for (int j = 1; j <= n; j++)
                {
                    if (str1[i - 1] == str2[j - 1])
                        curr[j] = prev[j - 1] + 1;
                    else
                        curr[j] = max(prev[j], curr[j - 1]);
                }
                prev = curr;
            }
            return m+n-2*curr[n];
	    
	} 
};

